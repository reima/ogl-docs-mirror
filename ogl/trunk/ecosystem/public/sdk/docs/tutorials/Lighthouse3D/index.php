<a href="http://www.lighthouse3d.com/opengl/viewfrustum/"><font color=#0000FF size=2><B>View Frustum Culling tutorial</B></font></A><BR>
View frustum culling is a simple way to add a boost to an OpenGL application. This tutorial shows how to extract the view frustum for a camera, and how to cull spheres and boxes that are outside the view frustum. Optimizations, notes, references and source code are also provided.<P>
