<a href="http://www.lighthouse3d.com/opengl/glsl/"><font color=#0000FF size=2><B>GLSL tutorial</B></font></A><BR>

An introduction to GLSL that not only covers shader writing, but also covers the graphics pipeline, the OpenGL Setup of an application using shaders.Variables, data types, and language syntax are also explored. Shader examples, namely for toon shading, lighting and texturing, are provided and source code is downloadable. Both ARB extensions and OpenGL 2.0 versions are included in the tutorial with color coded to help the reader to distinguish between them<P>
