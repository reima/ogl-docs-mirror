<h1>Documentation</h1>

<h2>The OpenGL Reference Pages</h2>

<p>The <a href="./man4/">reference pages for OpenGL 4</a> include
complete descriptions of all OpenGL commands contained in the OpenGL 4
Specification and the functions contained in the OpenGL Shading Language
4.40 Specification. </p>

<p>The <a href="./man3/">reference pages for OpenGL 3.3</a> include
complete descriptions of all OpenGL commands contained in the Core
Profile of the OpenGL 3.3 Specification.</p>

<p> The legacy <a href="./man2/">OpenGL 2.1 reference pages</a> include
descriptions of all OpenGL commands contained in the OpenGL 2.1
Specification, including commands that have been deprecated and removed
in more modern versions of OpenGL (but are still included in
compatibility profiles of OpenGL). Also included are descriptions of GLU
and GLX entry points. </p>

