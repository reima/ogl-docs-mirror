<a href="http://www.lighthouse3d.com/opengl/picking/"><font color=#0000FF size=2><B>Picking tutorial</B></font></A><BR>
The OpenGL API provides a mechanism for picking objects in a 3D scene. The Red Book proposes an alternative mechanism based on color coding. This tutorial will show you how to detect which objects are below the mouse or in a square region of the OpenGL window using both approaches. <P>
</TD>
