<H1>Tutorials</H1>

<P>The tutorials section includes a variety of contributions aimed at making OpenGL easy to learn by 
explanation and example.</P>

<P><A HREF="./ClockworkCoders/">ClockworkCoders</A> offer tutorials focusing on the OpenGL Shading 
Language. Learn how to set up the extensions, compile and link shaders, and make use of them to achieve
different rendering effects.</P>

<P><A HREF="./CodeColony/">CodeColony</A> has posted a number of interesting tutorials in the SDK
and has many more back at their web site. These tutorials range from your first triangle to much more
advanced techniques.</P>

<P><A HREF="./Lighthouse3D/">Lighthouse 3D</A> offers direct links to their tutorials covering the spectrum
from GLUT to GLSL, computer graphics math to stencil shadows.</P>

<p><a href="./OGLSamples">OpenGL Samples Pack</a> is a collection of OpenGL samples based on the OpenGL "core profile" specifications. The project aims to promote the new OpenGL features making easier version transitions for OpenGL programmers with a complementary documentation for the OpenGL specification.</p>

<P><A HREF="./TyphoonLabs/">TyphoonLabs</A> GLSL Course will show the student all about GLSL shaders, from
the language specs to advanced shader techniques, advancing through the needed steps to setup shaders in the
OpenGL host application.</P>

