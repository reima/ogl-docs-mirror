Several algorithms to generate artificial terrains, including the Mid Point Displacement algorithm, are presented.
This tutorial also covers the creation of a height map from an image, and its rendering. Normal computation and lighting simulation for both directional and positional lights is described in detail. A library to load TGA uncompressed images is also provided.<P>
