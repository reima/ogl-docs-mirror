<a href="http://www.lighthouse3d.com/opengl/billboarding/"><font color=#0000FF size=2><B>Billboarding tutorial</B></font></A><BR>
Billboarding is a technique that adjusts an object's orientation so that it "faces" some target, usually the camera. In this tutorial several techniques to implement billboarding are covered. From cheating billboards to true billboards. In all techniques both Cylindrical and Spherical versions are presented.<P>
</TD>
