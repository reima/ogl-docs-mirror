<p> The <a target="_new" href="http://www.opengl.org/registry/"> OpenGL
Registry</a>, hosted outside of the SDK, contains the formal
Specifications for OpenGL and the OpenGL Shading Language, as well as
for companion APIs like GLX and GLU. Specifications are the
authoritative documents about how OpenGL is intended to work. They are
also the most difficult to read, being written primarily for OpenGL
implementers, not developers using OpenGL; so you may find the reference
pages combined with tutorials to be a better choice. </p>

<p> The Registry also contains specifications for extensions describing
optional features of OpenGL, and header files defining interfaces for
extensions. Extension specifications are written as supplements to the
core OpenGL Specification. Most new features are first introduced as
extensions and only later become part of a core OpenGL version, so using
extensions is important to staying current with the evolution of the
API. </p>

